/* Creamos la vista para recuperar los ingredientes con cantidad baja.  */
CREATE VIEW [dbo].[v_InventarioMinimo]
AS
SELECT        Ig.nombre, Iv.cantidad AS Inventario, Iv.cant_min AS Minima
FROM            dbo.tbInventario AS Iv INNER JOIN
                         dbo.tbIngredientes AS Ig ON Ig.idIngrediente = Iv.idIngrediente
WHERE        (Iv.cantidad = Iv.cant_min)

GO

